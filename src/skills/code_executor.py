import io
import sys
import contextlib
import pandas as pd

def execute_python_code(code: str) -> str:
    """
    Executes Python code generated by the agent and returns the stdout/stderr.
    In a real production environment, this would run inside a Docker container or Firecracker microVM.
    For this prototype, it uses exec().
    """
    output_buffer = io.StringIO()
    
    # Safe-ish environment with pandas available
    env = {
        "pd": pd,
        "print": print
    }
    
    try:
        with contextlib.redirect_stdout(output_buffer), contextlib.redirect_stderr(output_buffer):
            exec(code, env)
        result = output_buffer.getvalue()
        if not result:
            return "Code executed successfully. No output."
        return result
    except Exception as e:
        return f"Error executing code: {str(e)}"
